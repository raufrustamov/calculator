public class MathFunction {


    public static double toAdd(double firstArgument, double secondArgument) {
        return firstArgument + secondArgument;
    }

    public static double toSubtract(double firstArgument, double secondArgument) {
        return firstArgument - secondArgument;
    }

    public static double toMultiply(double firstArgument, double secondArgument) {
        return firstArgument * secondArgument;
    }

    public static double toDivide(double firstArgument, double secondArgument) {
        return firstArgument / secondArgument;
    }
}
